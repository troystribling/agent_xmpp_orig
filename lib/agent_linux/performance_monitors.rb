class PerformanceMonitors

  #.........................................................................................................
  @monitors = {}
  #.........................................................................................................

  ###------------------------------------------------------------------------------------------------------
  class << self

    #.......................................................................................................
    attr_reader :monitors

    #.......................................................................................................
    def monitor(mon, mon_class, description, units, options = {})
      takes_object = options[:takes_object] || false
      @monitors[mon_class] = {:monitor => mon, :monitor_class => mon_class, :description => description, 
        :units => units, :takes_object => takes_object, :threshold => options[:threshold]} 
    end

  ###------------------------------------------------------------------------------------------------------
  end

############################################################################################################
# PerformanceMonitors
end