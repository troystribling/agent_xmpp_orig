class TaskManager

  ####------------------------------------------------------------------------------------------------------
  class << self
    
    #.........................................................................................................
    def performace_collector
      @@performace_collector
    end
    
    #.........................................................................................................
    def did_connect(client_connection)
      AgentXmpp.log_info "TaskManager.did_connect"
    end

    #.........................................................................................................
    def performance_collection(period)
      @@performace_collector = periodic_task(period) do 
        commands_class.cpu_stats(period)
      end
    end

    #.........................................................................................................
    def commands_class
      @@command_class ||= eval("#{`uname -s`.chop}Commands")
    end
    
    #.........................................................................................................
    def periodic_task(period)
      collector = lambda do
        while true
          yield
          sleep(period)
        end
      end
      EventMachine.defer(collector)
    end
  
  ###------------------------------------------------------------------------------------------------------
  end
   
############################################################################################################
# SystemCommands
end
