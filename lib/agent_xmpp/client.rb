##############################################################################################################
module AgentXmpp
  
  ############################################################################################################
  class Client

    #---------------------------------------------------------------------------------------------------------
    attr_reader :host, :port, :connection, :user
    #---------------------------------------------------------------------------------------------------------

    #.........................................................................................................
    def initialize(host, port=5222)
      @host, @port = host, port
    end

    #.........................................................................................................
    def connect
      EventMachine.run do
        EventMachine.connect(self.host, self.port, Connection, self.host, self.port) do |c|
          @connection = c
          @connection.host = self.host
          @connection.port = self.port
        end
      end
    end

  ############################################################################################################
  # Client
  end

##############################################################################################################
# AgentXmpp
end
