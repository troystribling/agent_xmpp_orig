module AgentXmpp
  
  ############################################################################################################
  class Controller

    #---------------------------------------------------------------------------------------------------------
    attr_reader :format, :params
    #---------------------------------------------------------------------------------------------------------

    #.........................................................................................................
    def initialize
    end
 
    #---------------------------------------------------------------------------------------------------------
    # actions
    #.........................................................................................................
    def execute
      AgentXmpp::logger.info "ACTION: AgentXmpp::Controller\#execute"
    end

    #.........................................................................................................
    def prev
      AgentXmpp::logger.info "ACTION: AgentXmpp::Controller\#prev"
    end

    #.........................................................................................................
    def next
      AgentXmpp::logger.info "ACTION: AgentXmpp::Controller\#next"
    end
    
    #.........................................................................................................
    def complete
      AgentXmpp::logger.info "ACTION: AgentXmpp::Controller\#complete"
    end

    #.........................................................................................................
    def cancel
      AgentXmpp::logger.info "ACTION: AgentXmpp::Controller\#cancel"
    end
    
    #---------------------------------------------------------------------------------------------------------
    # handle request
    #.........................................................................................................
    def handle_request(action, xmlns, params)
      @format = Format.new(xmlns)
      @params = params
      self.send(action)
    end

    #.........................................................................................................
    def result_for(&blk)
      @result_for_blk = blk
    end

    #.........................................................................................................
    def respond_to(&blk)
      Helper.send(:define_method, :respond_to, &blk)
      Helper.send(:define_method, :result_callback) do |result|
        data = self.respond_to(result)        
      end
      EventMachine.defer(@result_for_blk, Helper.new(self.format).method(:result_callback).to_proc)
    end
    
    
  ############################################################################################################
  # Controller
  end

