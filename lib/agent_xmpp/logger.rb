module AgentXmpp
  
  #---------------------------------------------------------------------------------------------------------
  @@debug = false
  #---------------------------------------------------------------------------------------------------------

  #.........................................................................................................
  def AgentXmpp::logger
    @@logger ||= Logger.new($stdout)
  end

  #.........................................................................................................
  def AgentXmpp::logger=(logger)
    @@logger = logger
  end

  #.........................................................................................................
  def AgentXmpp::debug=(debug)
    @@debug = debug
    if @@debug
      debuglog('Debugging mode enabled.')
    end
  end

  #.........................................................................................................
  def AgentXmpp::debug
    @@debug
  end

  #.........................................................................................................
  def AgentXmpp::debuglog(string)
    return if not @@debug
    logger.debug(string)
  end

##############################################################################################################
# AgentXmpp
end
