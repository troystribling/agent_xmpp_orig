module AgentXmpp
  
  #####-------------------------------------------------------------------------------------------------------
  class Format

    #---------------------------------------------------------------------------------------------------------
    attr_accessor :xmlns
    #---------------------------------------------------------------------------------------------------------

    #.........................................................................................................
    def initialize(xmlns)
      @xmlns = xmlns
    end

    #.........................................................................................................
    def x_data(&blk)
      call_block_for_xmlns('jabber:x:data', &blk)
    end

    #.........................................................................................................
    def json(&blk)
      call_block_for_xmlns('jabber:x:json', &blk)
    end

  private
  
    #.........................................................................................................
    def call_block_for_xmlns(check_xmlns, &blk)
      if xmlns.eql?(check_xmlns)
        if blk
          blk.call
        end
      end
    end
  
  #### Format
  end

#### AgentXmpp
end
