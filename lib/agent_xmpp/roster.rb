module AgentXmpp
  
  ############################################################################################################
  class Roster

    #.........................................................................................................
    def initialize(contacts)
      @items = Hash.new{|hash, key| hash[key] = {:activated => false}}
      contacts.each{|c| @items[c]} 
    end

    ##################################################################################
    def method_missing(meth, *args, &blk)
      @items.send(meth, *args, &blk)
    end

  ############################################################################################################
  # Roster
  end

##############################################################################################################
# AgentXmpp
end
