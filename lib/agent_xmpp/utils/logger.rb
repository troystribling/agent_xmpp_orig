module AgentXmpp
  
  #.........................................................................................................
  def self.logger
    @@logger ||= Logger.new($stdout)
  end

  #.........................................................................................................
  def self.logger=(logger)    
    @@logger = logger
  end

  #.........................................................................................................
  def self.log_info(msg)
    self.logger.info msg
  end

  #.........................................................................................................
  def self.log_warn(msg)
    self.logger.warn msg
  end

#### AgentXmpp
end
